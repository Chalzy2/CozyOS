/**
 * CozyOS Enterprise Framework — CozyQuestionEngine
 * File Reference: core/modules/builder/question-engine.js
 * Version: 1.0.0-ENTERPRISE
 * Layer: Core / Code Generation — AI Question Engine
 *
 * RESPONSIBILITY
 *   Turns RequirementAnalyzer's real gap-detection output into concrete
 *   clarifying questions, and accepts answers in whatever format the
 *   developer already has them in (typed text, or pre-extracted PDF/
 *   screenshot text via UnderstandingEngine/CozyOCR — never re-analyzing
 *   those formats itself).
 *
 * WHAT THIS MODULE ACTUALLY DOES
 *   - generateQuestions(): a real, deterministic map from each missing-
 *     checklist item (RequirementAnalyzer/UnderstandingEngine's Gap
 *     Detector) to a concrete question string. Not adaptive AI reasoning
 *     — a fixed, disclosed lookup table.
 *   - submitAnswer(): stores a real answer against a question, with an
 *     optional sourceFormat tag (text/pdf/screenshot) purely for
 *     record-keeping; extraction itself is never redone here.
 *   - reanalyzeWithAnswers(): feeds the accumulated Q&A back into
 *     RequirementAnalyzer.analyzeRequirement() by appending the answers
 *     as additional text — reusing that engine's real extraction rather
 *     than duplicating it.
 *
 * WHAT THIS MODULE DOES NOT DO (Honest Capability Rule)
 *   - Questions are NOT adaptively generated by reasoning about the
 *     specific project — they come from a fixed table keyed to the same
 *     checklist UnderstandingEngine already uses. This is disclosed
 *     everywhere, not presented as genuine AI-driven questioning.
 *   - Never re-extracts PDF/screenshot content — the caller supplies
 *     already-extracted text (via UnderstandingEngine/CozyOCR); this
 *     module only records which format it came from.
 *
 * OPTIONAL INTEGRATIONS
 *   RequirementAnalyzer — the gap report this reads, and the engine
 *                        reanalyzeWithAnswers() calls back into.
 *   CozyMemory          — records question sessions (Memory Read/Write),
 *                        same pattern as every other engine.
 *   ServiceRegistry     — registerCoordinator(), with retry.
 */

(function () {
    "use strict";

    window.CozyOS = window.CozyOS || {};
    const QE_VERSION = "1.0.0-ENTERPRISE";
    const FORBIDDEN_KEYS = new Set(["__proto__", "constructor", "prototype"]);

    // Real, fixed question table keyed to UnderstandingEngine's own gap
    // checklist IDs — disclosed as a fixed lookup, not adaptive reasoning.
    const QUESTION_TEMPLATES = Object.freeze({
        "user-roles": "Which user roles will use this system (e.g. admin, staff, customer)?",
        "permissions": "What can each role do vs. not do?",
        "audit-log": "Do you need a history/audit trail of who changed what?",
        "offline-mode": "Must this work without an internet connection?",
        "backup": "How should data be backed up and restored?",
        "plugin-system": "Should third parties be able to extend this later?",
        "security-policy": "Are there specific security/compliance requirements?",
        "multi-tenant": "Will multiple organizations share this, or is it single-tenant?",
        "rate-limiting": "Are there usage limits or quotas to enforce?",
        "data-export": "Do users need to export their data (CSV, PDF, etc.)?"
    });

    class CozyOSQuestionEngine {
        #sessions = new Map();
        #auditLogs = [];
        #timelineEvents = [];
        #listeners = new Map();
        #onceWrapped = new Map();
        #diagnostics = { sessionsCreated: 0, questionsGenerated: 0, answersSubmitted: 0, reanalysesRun: 0, errorsHidden: 0, eventsEmitted: 0, memoryBaseline: 2.8 };

        getVersion() { return QE_VERSION; }

        #deepClone(value) {
            if (typeof structuredClone === "function") { try { return structuredClone(value); } catch (_err) { /* fall through */ } }
            try { return JSON.parse(JSON.stringify(value)); } catch (_err) { return value; }
        }

        #escapeHtml(value) {
            const str = String(value === undefined || value === null ? "" : value);
            return str.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;").replace(/'/g, "&#39;");
        }

        #generateId(prefix) {
            const raw = typeof crypto !== "undefined" && crypto.randomUUID ? crypto.randomUUID() : `${Date.now()}-${Math.random().toString(16).slice(2)}`;
            return `${prefix}_${raw}`;
        }

        #logAudit(action, msg) {
            this.#auditLogs.push(Object.freeze({ id: this.#generateId("aud"), timestamp: new Date().toISOString(), action, msg }));
            if (this.#auditLogs.length > 500) this.#auditLogs.shift();
        }

        #logTimeline(label) {
            this.#timelineEvents.push(Object.freeze({ time: new Date().toISOString(), label }));
            if (this.#timelineEvents.length > 500) this.#timelineEvents.shift();
        }

        getAuditLog(predicate) { const list = this.#auditLogs.map(e => this.#deepClone(e)); return Object.freeze(predicate ? list.filter(predicate) : list); }
        getTimeline(predicate) { const list = this.#timelineEvents.map(e => this.#deepClone(e)); return Object.freeze(predicate ? list.filter(predicate) : list); }

        on(eventName, handler) {
            if (typeof eventName !== "string" || !eventName.trim()) throw new TypeError("[QuestionEngine] on(): eventName must be a non-empty string.");
            if (typeof handler !== "function") throw new TypeError("[QuestionEngine] on(): handler must be a function.");
            if (!this.#listeners.has(eventName)) this.#listeners.set(eventName, new Set());
            this.#listeners.get(eventName).add(handler);
            return () => this.off(eventName, handler);
        }
        off(eventName, handler) {
            const set = this.#listeners.get(eventName);
            if (!set) return false;
            const wrapped = this.#onceWrapped.get(handler);
            const removed = set.delete(handler) || (wrapped ? set.delete(wrapped) : false);
            if (set.size === 0) this.#listeners.delete(eventName);
            return removed;
        }
        once(eventName, handler) {
            if (typeof handler !== "function") throw new TypeError("[QuestionEngine] once(): handler must be a function.");
            const wrapper = (payload) => { this.off(eventName, handler); this.#onceWrapped.delete(handler); handler(payload); };
            this.#onceWrapped.set(handler, wrapper);
            this.on(eventName, wrapper);
        }
        emit(eventName, payload) {
            if (typeof eventName !== "string" || !eventName.trim()) { this.#diagnostics.errorsHidden++; return false; }
            const set = this.#listeners.get(eventName);
            this.#diagnostics.eventsEmitted++;
            if (!set || set.size === 0) return false;
            let safePayload = payload;
            try { safePayload = this.#deepClone(payload); } catch (_err) { safePayload = payload; }
            for (const fn of Array.from(set)) { try { fn(safePayload); } catch (_err) { this.#diagnostics.errorsHidden++; } }
            return true;
        }

        /**
         * generateQuestions(analysisId)
         *   Reads a real RequirementAnalyzer analysis's missingInformation
         *   (already keyed to the same checklist IDs UnderstandingEngine's
         *   Gap Detector uses), maps each to a concrete question via the
         *   fixed QUESTION_TEMPLATES table, and opens a session to collect
         *   answers.
         */
        generateQuestions(analysisId) {
            const ra = window.CozyOS.RequirementAnalyzer;
            if (!ra) throw new Error("[QuestionEngine] RequirementAnalyzer is not connected.");
            const analysis = ra.getAnalysis(analysisId);
            if (!analysis) throw new Error(`[QuestionEngine] generateQuestions(): no analysis "${analysisId}".`);

            const ue = window.CozyOS.UnderstandingEngine;
            const checklist = ue && typeof ue.listChecklist === "function" ? ue.listChecklist() : [];
            const idByLabel = new Map(checklist.map(c => [c.label, c.id]));

            const questions = analysis.missingInformation.map(label => {
                const id = idByLabel.get(label);
                return { id: this.#generateId("q"), gapId: id || null, gapLabel: label, question: (id && QUESTION_TEMPLATES[id]) || `Please clarify: ${label}.`, answer: null, sourceFormat: null };
            });

            const sessionId = this.#generateId("qsession");
            const session = { sessionId, analysisId, createdAt: new Date().toISOString(), questions, completed: false };
            this.#sessions.set(sessionId, session);
            this.#diagnostics.sessionsCreated++;
            this.#diagnostics.questionsGenerated += questions.length;
            this.#logAudit("QUESTIONS_GENERATED", `${sessionId}: ${questions.length} question(s) for analysis ${analysisId}`);
            this.#logTimeline(`Questions generated for ${analysis.domain}`);
            this.emit("questions:generated", { sessionId, count: questions.length });
            return this.#deepClone(session);
        }

        getSession(sessionId) { const s = this.#sessions.get(sessionId); return s ? this.#deepClone(s) : null; }
        listSessions(predicate) { const list = Array.from(this.#sessions.values()).map(s => this.#deepClone(s)); return Object.freeze(predicate ? list.filter(predicate) : list); }

        /**
         * submitAnswer(sessionId, questionId, answerText, sourceFormat)
         *   sourceFormat is a record-keeping tag only ("text"|"pdf"|
         *   "screenshot"|"external-ai") — this method never re-extracts
         *   anything; the caller supplies already-extracted text (e.g.
         *   from UnderstandingEngine.analyzePDF()/analyzeScreenshot(), or
         *   pasted from an external AI chat).
         */
        submitAnswer(sessionId, questionId, answerText, sourceFormat = "text") {
            const session = this.#sessions.get(sessionId);
            if (!session) throw new Error(`[QuestionEngine] submitAnswer(): no session "${sessionId}".`);
            const q = session.questions.find(q => q.id === questionId);
            if (!q) throw new Error(`[QuestionEngine] submitAnswer(): no question "${questionId}" in session "${sessionId}".`);
            q.answer = this.#escapeHtml(answerText);
            q.sourceFormat = sourceFormat;
            session.completed = session.questions.every(q => q.answer !== null);
            this.#diagnostics.answersSubmitted++;
            this.#logAudit("ANSWER_SUBMITTED", `${sessionId}/${questionId} (${sourceFormat})`);
            this.emit("answer:submitted", { sessionId, questionId });
            return this.#deepClone(q);
        }

        /**
         * reanalyzeWithAnswers(sessionId)
         *   Feeds every answered question back into RequirementAnalyzer
         *   by appending "Q: ... A: ..." lines to the original text and
         *   calling analyzeRequirement() again — reusing that engine's
         *   real extraction, never duplicating it here.
         */
        reanalyzeWithAnswers(sessionId) {
            const session = this.#sessions.get(sessionId);
            if (!session) throw new Error(`[QuestionEngine] reanalyzeWithAnswers(): no session "${sessionId}".`);
            const ra = window.CozyOS.RequirementAnalyzer;
            if (!ra) throw new Error("[QuestionEngine] RequirementAnalyzer is not connected.");
            const analysis = ra.getAnalysis(session.analysisId);
            if (!analysis) throw new Error(`[QuestionEngine] reanalyzeWithAnswers(): original analysis "${session.analysisId}" no longer exists.`);

            const qaText = session.questions.filter(q => q.answer).map(q => `${q.question} ${q.answer}`).join(" ");
            const combinedText = `${analysis.rawText} ${qaText}`;
            this.#diagnostics.reanalysesRun++;
            const newAnalysis = ra.analyzeRequirement(combinedText, { domain: analysis.domain });

            if (window.CozyOS.CozyMemory) {
                try { window.CozyOS.CozyMemory.saveMemory("Project", `qa-session-${sessionId}`, session, { tags: ["question-session", analysis.domain] }); } catch (_err) { /* additive only */ }
            }
            this.#logAudit("REANALYZED_WITH_ANSWERS", `${sessionId} -> new analysis ${newAnalysis.id}`);
            this.emit("questions:reanalyzed", { sessionId, newAnalysisId: newAnalysis.id });
            return newAnalysis;
        }

        isVersionCompatible(version) {
            const a = /^v?(\d+)\./.exec(String(QE_VERSION));
            const b = /^v?(\d+)\./.exec(String(version || ""));
            if (!a || !b) return false;
            return a[1] === b[1];
        }

        getDiagnosticsReport() {
            return this.#deepClone({
                moduleVersion: QE_VERSION, ...this.#diagnostics, sessionCount: this.#sessions.size,
                dependencies: [{ name: "RequirementAnalyzer", required: true, purpose: "Gap report source and reanalysis target." }],
                integrationCount: window.CozyOS.RequirementAnalyzer ? 1 : 0,
                auditLogCount: this.#auditLogs.length, timelineEventCount: this.#timelineEvents.length
            });
        }

        exportSnapshot() { return this.#deepClone({ version: QE_VERSION, exportedAt: new Date().toISOString(), sessions: Array.from(this.#sessions.values()) }); }
        importSnapshot(snapshot, { mergeStrategy = "merge" } = {}) {
            if (!snapshot || typeof snapshot !== "object") throw new TypeError("[QuestionEngine] importSnapshot(): snapshot must be an object.");
            if (mergeStrategy === "replace") this.#sessions.clear();
            let imported = 0;
            for (const s of (snapshot.sessions || [])) { if (s && s.sessionId && !this.#sessions.has(s.sessionId)) { this.#sessions.set(s.sessionId, s); imported++; } }
            return { imported };
        }
        isSnapshotCompatible(snapshot) { return !!(snapshot && typeof snapshot.version === "string" && snapshot.version.split(".")[0] === QE_VERSION.split(".")[0]); }
    }

    if (window.CozyOS.QuestionEngine && typeof window.CozyOS.QuestionEngine.getVersion === "function") {
        const existingVersion = window.CozyOS.QuestionEngine.getVersion();
        if (existingVersion !== QE_VERSION) throw new Error(`[CozyOS Framework Execution Error] VERSION_CONFLICT: QuestionEngine existing v${existingVersion} conflicts with load target v${QE_VERSION}.`);
        return;
    }
    window.CozyOS.QuestionEngine = new CozyOSQuestionEngine();

    (function registerWithServiceRegistry(descriptor) {
        function attempt() {
            if (typeof window.CozyOS.registerCoordinator !== "function") return false;
            try { window.CozyOS.registerCoordinator(descriptor); } catch (_err) { /* non-fatal */ }
            return true;
        }
        if (attempt()) return;
        if (!Object.prototype.hasOwnProperty.call(window.CozyOS, "__pendingCoordinatorRegistrations")) {
            Object.defineProperty(window.CozyOS, "__pendingCoordinatorRegistrations", { value: [], writable: true, enumerable: false, configurable: true });
        }
        window.CozyOS.__pendingCoordinatorRegistrations.push(descriptor);
        let attempts = 0;
        const intervalId = setInterval(() => {
            attempts++;
            if (attempt() || attempts >= 200) {
                clearInterval(intervalId);
                const idx = window.CozyOS.__pendingCoordinatorRegistrations.indexOf(descriptor);
                if (idx !== -1) window.CozyOS.__pendingCoordinatorRegistrations.splice(idx, 1);
            }
        }, 250);
    })({ name: "QuestionEngine", category: "Code Generation", icon: "question-engine.svg", description: "CozyBuilder's AI Question Engine — real fixed-template questions from RequirementAnalyzer's gap detection; accepts multi-format answers without re-extracting them." });
})();
